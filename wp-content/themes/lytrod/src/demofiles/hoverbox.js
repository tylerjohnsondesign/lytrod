for(let i = 1;i<=18;i++){
        let themainbox = document.querySelector(`.pagebox${i}a`)
        let headerbox = document.querySelector(`.pagebox${i}`)
        themainbox.addEventListener('mouseenter',function(){
            headerbox.style.backgroundColor ="#3f3d56"
            themainbox.style.fill="#63607a"
        });
        themainbox.addEventListener('mouseout',function(){
            headerbox.style.backgroundColor ="#083155"
            themainbox.style.fill="#f3f3f3"
        });
        headerbox.addEventListener('mouseenter',function(){
            themainbox.style.fill="#7e7c8d"
            headerbox.style.backgroundColor ="#3f3d56"
        })
        headerbox.addEventListener('mouseout',function(){
            themainbox.style.fill="#f3f3f3"
            headerbox.style.backgroundColor ="#083155"
        })
}

